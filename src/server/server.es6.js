////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// imports                                                                                                            //
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

/* argument parsing (could auto-exit the process when --help is asked) */
import config from '../config.es6.js';

/* the server implementation */
import getServer from '../server.es6.js';


////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// starting the server                                                                                                //
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

getServer(`${__dirname}/../`, config).then((server) => {

	server.listen(config.port, () => {
		console.log(`listening on port ${config.port}`);
	});

}).catch((err) => {

	console.error('Something went wrong while trying to start the server.', err);

});
